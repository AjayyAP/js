let x = 10;
console.log("initial value of x:", x);

// using +=

x+=5;
console.log("After x += ",x);



//using -=

x-=3;
console.log("After x -= ",x);

// using *=

x*=2;
console.log("After x *= ",x);


// using /=

x/=2;
console.log("After x /= ",x);


// using %=

x%=2;
console.log("After x %= ",x);






